// ---------------------------------------------------------------------------
// Recurring transactions
// ---------------------------------------------------------------------------

// MVP types only. Expand when adding sell / deposit / withdraw / interest.
export type RecurringTransactionType = 'buy' | 'fee'

export type RecurringFrequency = 'monthly' | 'weekly' | 'quarterly'

export type RecurringTransaction = {
  id: string
  user_id: string
  investment_id: string
  type: RecurringTransactionType
  frequency: RecurringFrequency
  // day_of_month: 1–28, set when frequency = 'monthly' | 'quarterly'
  day_of_month: number | null
  // day_of_week: 0–6 (0 = Monday), set when frequency = 'weekly'
  day_of_week: number | null
  start_date: string              // 'YYYY-MM-DD'
  end_date: string | null         // null = indefinite
  // For 'buy': exactly one of fixed_quantity / fixed_amount is non-null.
  // For 'fee': fixed_amount is non-null, fixed_quantity is null.
  fixed_quantity: number | null
  fixed_amount: number | null
  fixed_amount_currency: string | null
  fee: number
  fee_currency: string
  notes: string | null
  active: boolean
  // null = never generated; advances by one period each cron run.
  last_generated_date: string | null  // 'YYYY-MM-DD'
  created_at: string
  updated_at: string
}

